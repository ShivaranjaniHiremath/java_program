class Incop{
		public static void main(String args[]){
		
			System.out.println("illustrating increament operations");
			
			int a=7;
			int b=4;
			int preinc=++a;
			int postinc=b++;
			
			
			System.out.println("the value of preinc is:" + preinc);
			
			System.out.println("the value of postinc is:" + postinc);
			
		}
		
}
